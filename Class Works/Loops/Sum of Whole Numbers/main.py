age = int(input("Enter a age: "))

if age <= 3:
    print("You're a infant")

elif age <= 5:
    print("You're a toddler")

elif age <= 12:
    print("You are a kid")

elif age <= 19:
    print("You are a teenager")

elif age <= 28:
    print("You are an adult")

elif age >= 60:
    print("You are old")